package main

import "fmt"

// This function demonstrates common Go data types.
func dataTypesExample() {
    // String data type for text.
    name := "Manas"

    // Integer data type for whole numbers.
    age := 25

    // Float data type for decimal numbers.
    salary := 14.5

    // Boolean data type for true/false values.
    isDeveloper := true

    // Print each value to the console.
    fmt.Println(name)
    fmt.Println(age)
    fmt.Println(salary)
    fmt.Println(isDeveloper)
}
