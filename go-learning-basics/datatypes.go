package main // package declaration

import "fmt" // statement

// This function demonstrates common Go data types.
func dataTypesExample() { // function declaration
	// String data type for text.
	name := "Manas" // declare and initialize variable

	// Integer data type for whole numbers.
	age := 25 // declare and initialize variable

	// Float data type for decimal numbers.
	salary := 14.5 // declare and initialize variable

	// Boolean data type for true/false values.
	isDeveloper := true // declare and initialize variable

	// Print each value to the console.
	fmt.Println(name)        // statement
	fmt.Println(age)         // statement
	fmt.Println(salary)      // statement
	fmt.Println(isDeveloper) // statement
} // statement
